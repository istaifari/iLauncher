void vga_WriteResolutionRegister(unsigned char *regs)
{
	unsigned i;
	outb(VGA_MISC_WRITE, *regs);
	regs++;
	for (i = 0; i < VGA_NUM_SEQ_REGS; i++)
	{
		outb(VGA_SEQ_INDEX, i);
		outb(VGA_SEQ_DATA, *regs);
		regs++;
	}
	outb(VGA_CRTC_INDEX, 0x03);
	outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) | 0x80);
	outb(VGA_CRTC_INDEX, 0x11);
	outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) & ~0x80);
	regs[0x03] |= 0x80;
	regs[0x11] &= ~0x80;
	for (i = 0; i < VGA_NUM_CRTC_REGS; i++)
	{
		outb(VGA_CRTC_INDEX, i);
		outb(VGA_CRTC_DATA, *regs);
		regs++;
	}
	for (i = 0; i < VGA_NUM_GC_REGS; i++)
	{
		outb(VGA_GC_INDEX, i);
		outb(VGA_GC_DATA, *regs);
		regs++;
	}
	for (i = 0; i < VGA_NUM_AC_REGS; i++)
	{
		(void)inb(VGA_INSTAT_READ);
		outb(VGA_AC_INDEX, i);
		outb(VGA_AC_WRITE, *regs);
		regs++;
	}
	(void)inb(VGA_INSTAT_READ);
	outb(VGA_AC_INDEX, 0x20);
}

void vga_drawimage(int x, int y, int w, int h, unsigned pixels[])
{
	long long int i = 0, l = 0, j = 0;
	for (l = j = 0; l < h; l++)
	{
		for (i = 0; i < w; i++, j++)
		{
			if (pixels[j] != 0x40)
				vga_putpixel(x + i, y + l, pixels[j]);
		}
	}
}

void vga_fillRect(unsigned x, unsigned y, unsigned w, unsigned h, unsigned c)
{
	for (int Y = y; Y < y + h; Y++)
	{
		for (int X = x; X < x + w; X++)
		{
			vga_putpixel(X, Y, c);
		}
	}
}

void vga_drawchar(char chr, unsigned x, unsigned y, unsigned c, unsigned char font[], unsigned font_w, unsigned font_h, bool flip)
{
	int cx, cy;
	int mask[8];
	if (flip)
	{
		mask[0] = 1;
		mask[1] = 2;
		mask[2] = 4;
		mask[3] = 8;
		mask[4] = 16;
		mask[5] = 32;
		mask[6] = 64;
		mask[7] = 128;
	}
	else
	{
		mask[7] = 1;
		mask[6] = 2;
		mask[5] = 4;
		mask[4] = 8;
		mask[3] = 16;
		mask[2] = 32;
		mask[1] = 64;
		mask[0] = 128;
	}
	unsigned char *gylph = font + (int)chr * font_h;

	for (cy = 0; cy < font_h; cy++)
	{
		for (cx = 0; cx < font_w; cx++)
		{
			if (gylph[cy] & mask[cx])
				vga_putpixel(x + cx, y + cy, c);
		}
	}
}

void vga_drawtext(char *text, unsigned color, unsigned x, unsigned y, unsigned char font[], unsigned font_w, unsigned font_h)
{
	long i, x_pos, y_pos, col, row, stop;
	char chr;

	int len = strlen(text);
	int font_width = font_w, font_height = font_h;
	row = x;
	col = y;
	stop = 0;
	for (i = 0; i < len; i++)
	{
		switch (text[i])
		{
		case '\n':
			row++;
			col = 0;
			chr = 0;
			break;

		case '\r':
			chr = 0;
			break;

		case '\t':
			chr = 0;
			col += 4 - col % 4;
			break;

		case '\0':
			stop = 1;
			chr = 0;
			break;

		default:
			col++;
			chr = text[i];
			break;
		}

		if (stop)
		{
			break;
		}

		if (((col - 1) * font_width) >= vga_width)
		{
			row++;
			col = 0;
			chr = 0;
		}

		vga_drawchar(chr, ((col - 1) * font_width), (row * font_height), color, font, font_w, font_h, false);
	}
}

void vga_clear()
{
	unsigned x, y;
	for (y = 0; y < vga_height; y++)
	{
		for (x = 0; x < vga_width; x++)
		{
			vga_putpixel(x, y, 0x00);
		}
	}
}

unsigned vga_getresolution(int select)
{
	if (select == 1)
	{
		return vga_width;
	}
	else if (select == 2)
	{
		return vga_height;
	}
	else
	{
		return 0;
	}
}

