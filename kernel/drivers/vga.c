// arrays

char vga_ch[vga_buf_resolution];
char vga_color[vga_buf_resolution];
long vga_x[vga_buf_resolution];
long vga_y[vga_buf_resolution];

void vga_add(char ch, char color, long x, long y)
{
	long x_pos = x;
	if (x_pos < 0)
		x_pos = 0;
	if (x_pos >= 80)
		x_pos = 79;

	long y_pos = y;
	if (y_pos < 0)
		y_pos = 0;
	if (y_pos >= 25)
		y_pos = 24;
	vga_update_stat--;
	vga_ch[vga_elem_length] = ch;
	vga_color[vga_elem_length] = color;
	vga_x[vga_elem_length] = x_pos;
	vga_y[vga_elem_length] = y_pos;
	vga_elem_length++;
	vga_update_stat++;
}

void vga_addUpdate(int id, char ch, char color, long x, long y)
{
	vga_update_stat--;
	vga_ch[id] = ch;
	vga_color[id] = color;
	vga_x[id] = x;
	vga_y[id] = y;
	vga_update_stat++;
}

void vga_addRemove(int id)
{
	vga_update_stat--;
	char ch;
	char color = 0x00;
	long x = 0;
	long y = 0;

	vga_ch[id] = ch;
	vga_color[id] = color;
	vga_x[id] = x;
	vga_y[id] = y;
	vga_elem_length--;
	vga_update_stat++;
}

