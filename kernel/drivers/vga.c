void vga_clear()
{
	unsigned x, y;
	for (y = 0; y < vga_height; y++)
	{
		for (x = 0; x < vga_width; x++)
		{
			vga_putpixel(x, y, 0x00);
		}
	}
}

long int vga_getresolution(int select)
{
	if (select == 1)
	{
		return vga_width;
	}
	else if (select == 2)
	{
		return vga_height;
	}
	else
	{
		return 0;
	}
}

